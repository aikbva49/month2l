"""Конвертация текста
Задание: Написать программу, которая запрашивает ввести слово, затем выводит это слово в обратной раскладке клавиатуры.
(Пример на картинке)
Условия программы:
Программа должна работать в бесконечном цикле с возможностью выхода
Запрашивать у пользователя любое слово на латинице или кириллице, а также смешанно.
Считывать строчные и прописные буквы, вне зависимости от ввода результат должен возвращаться в нижнем регистре
Программа также должна работать со смешанными языками

ВЫПОЛНИТЬ В PYTHON ФАЙЛАЕ И ПРИКРЕПИТЬ К "КОНТРОЛЬНОЙ РАБОТЕ №2" НА ПЛАТФОРМЕ!
15 баллов

"""
rus = "ёйцукенгшщзхъфывапролджэячсмитьбю"
eng = "`qwertyuiop[]asdfghjkl;'zxcvbnm,."

while True:
    text = input("Введите слово (для выхода нажмите 'q'): ")
    if text.lower() == 'q':
        print("Программа завершена.")
        break
    text_lower = text.lower()
    result = " "
    for w in text_lower:
        if w in rus:
            index = rus.index(w)
            result += eng[index]
        elif w in eng:
            index = eng.index(w)
            result += rus[index]
        else:
            result += w
    print("Результат:", result)


