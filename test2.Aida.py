def price_discount(p_hm, p_t):

    if 65 <= p_hm <= 80 and 75 <= p_t <= 100:
        return 3000
    elif 65 <= p_hm <= 80 and 55 <= p_t <= 74:
        return 2500
    elif 45 <= p_hm <= 64 and 75 <= p_t <= 100:
        return 2000
    elif 45 <= p_hm <= 64 and 55 <= p_t <= 74:
        return 1000
    else:
        return 0
homework = int(input("Введите баллы за ДЗ: "))
test = int(input("Введите баллы за тест: "))
discount = price_discount(homework, test)
print("Сумма скидки:", discount, "сом")
